#include "libft.h"

int	ft_sqrt(int nb)
{
	int a;
	int nbr;

	a = 1;
	nbr = nb;
	while (nbr > a)
	{
		nbr = (nbr + a) / 2;
		a = nb / nbr;
	}
	if (nbr * nbr == nb)
		return (nbr);
	return (0);
}
